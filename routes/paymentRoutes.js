const express = require("express");
const router = express.Router();

const paymentController = require("../controllers/paymentController");
const webhookController = require("../controllers/paymentWebhookController");

const verifyFirebaseToken = require("../middlewares/auth");

//////////////////////////////////////////////////////
// TENANT PAYMENT (UPI QR GENERATION)
//////////////////////////////////////////////////////

router.post(
  "/create-payment",
  verifyFirebaseToken,
  paymentController.createPayment
);

//////////////////////////////////////////////////////
// USER PAYMENT CONFIRMATION
//////////////////////////////////////////////////////

// User clicks "I HAVE PAID"
router.post(
  "/confirm-payment",
  verifyFirebaseToken,
  paymentController.confirmPayment
);

//////////////////////////////////////////////////////
// USER SUBMIT UTR
//////////////////////////////////////////////////////

router.post(
  "/submit-utr",
  verifyFirebaseToken,
  paymentController.submitUTR
);

//////////////////////////////////////////////////////
// AUTO BANK MATCH (OPTIONAL)
//////////////////////////////////////////////////////

router.post(
  "/match-bank-transaction",
  verifyFirebaseToken,
  paymentController.matchBankTransaction
);

//////////////////////////////////////////////////////
// PAYMENT WEBHOOK (future payment gateway)
//////////////////////////////////////////////////////

router.post(
  "/webhook",
  webhookController.paymentWebhook
);

//////////////////////////////////////////////////////
// ADMIN – GET SUBMITTED PAYMENTS (IMPORTANT)
//////////////////////////////////////////////////////

router.get(
  "/admin/payments",
  verifyFirebaseToken,
  paymentController.getSubmittedPayments
);

//////////////////////////////////////////////////////
// ADMIN – VERIFY PAYMENT
//////////////////////////////////////////////////////

router.put(
  "/admin/payments/:orderId/verify",
  verifyFirebaseToken,
  paymentController.verifyPayment
);

//////////////////////////////////////////////////////
// ADMIN – REJECT PAYMENT
//////////////////////////////////////////////////////

router.put(
  "/admin/payments/:orderId/reject",
  verifyFirebaseToken,
  paymentController.rejectPayment
);

//////////////////////////////////////////////////////
// ADMIN – PENDING OWNER SETTLEMENT
//////////////////////////////////////////////////////

router.get(
  "/admin/pending-settlements",
  verifyFirebaseToken,
  paymentController.getPendingSettlements
);

//////////////////////////////////////////////////////
// ADMIN – MARK OWNER PAID
//////////////////////////////////////////////////////

router.put(
  "/admin/mark-settled/:bookingId",
  verifyFirebaseToken,
  paymentController.markAsSettled
);

//////////////////////////////////////////////////////
// ADMIN – FINANCE SUMMARY
//////////////////////////////////////////////////////

router.get(
  "/admin/finance-summary",
  verifyFirebaseToken,
  paymentController.getFinanceSummary
);

//////////////////////////////////////////////////////
// ADMIN – SETTLEMENT HISTORY
//////////////////////////////////////////////////////

router.get(
  "/admin/settlement-history",
  verifyFirebaseToken,
  paymentController.getSettlementHistory
);

module.exports = router;